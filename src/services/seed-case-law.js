export async function seedSyntheticCaseLaw(force = false) {
  // Check if we already have enough cases (skip check if force=true)
  if (!force && isDbAvailable()) {
    try {
      const { rows } = await pool.query('SELECT COUNT(*) as cnt FROM hivelaw.case_law');
      if (parseInt(rows[0].cnt, 10) > 10) {
        console.log(`  [Seed] Case law table already has ${rows[0].cnt} entries — skipping synthetic seed`);
        return;
      }
    } catch (err) {
      console.error('[seed] Count check failed:', err.message);
    }
  }

  console.log('  [Seed] Seeding 50 synthetic case law precedents...');

  for (let i = 0; i < SYNTHETIC_CASES.length; i++) {
    const s = SYNTHETIC_CASES[i];
    const now = new Date();
    // Stagger dates 5-180 days ago for realistic spread
    const daysAgo = 5 + Math.floor((i / SYNTHETIC_CASES.length) * 175);
    const filedDate = new Date(now.getTime() - daysAgo * 86400000);

    const caseLaw = createCaseLaw({
      disputeId: `dis_synth_${String(i + 1).padStart(3, '0')}`,
      category: s.category,
      jurisdiction: s.jurisdiction,
      summary: s.summary,
      rulingSummary: s.rulingSummary,
      keyFactors: s.keyFactors,
      outcome: s.outcome,
      damagesUsdc: s.damagesUsdc,
      jurisdictionApplicability: s.jurisdictionApplicability,
    });
    caseLaw.filed_at = filedDate.toISOString();
    await addCase(caseLaw, 'synthetic');
  }

  console.log('  [Seed] 50 synthetic case law precedents seeded successfully');
}
